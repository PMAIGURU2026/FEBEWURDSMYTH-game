/**
 * Build-time script: generates frontend/js/env-config.js from Vercel env vars.
 * Run automatically during Vercel build (see vercel.json buildCommand).
 * Local devs: manually fill in frontend/js/env-config.js (it is gitignored).
 */

const fs = require('fs');
const path = require('path');

const url = process.env.SUPABASE_URL || '';
const anonKey = process.env.SUPABASE_ANON_KEY || '';

if (!url || !anonKey) {
  console.warn(
    '[generate-frontend-env] SUPABASE_URL or SUPABASE_ANON_KEY env var not set — ' +
    'env-config.js will have empty values. ' +
    'Direct Supabase reads from the frontend will be unavailable; the API handles everything else.'
  );
}

const content = `/* AUTO-GENERATED by scripts/generate-frontend-env.js — do not edit */
/* This file is gitignored. Real values come from Vercel environment variables. */
const ENV_CONFIG = {
    SUPABASE_URL: ${JSON.stringify(url)},
    SUPABASE_ANON_KEY: ${JSON.stringify(anonKey)}
};
`;

const dest = path.join(__dirname, '..', 'frontend', 'js', 'env-config.js');
fs.writeFileSync(dest, content, 'utf8');
console.log('[generate-frontend-env] Wrote', dest);
